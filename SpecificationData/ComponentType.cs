namespace SpecificationData;

/// Тип компонента в спецификации: Изделие, Узел или Деталь.
// Типы компонентов в спецификации: Изделие, Узел, Деталь.
// Используется в `SpecificationManager` для определения поведения записи.
public enum ComponentType
{
    /// Изделие — может содержать узлы и детали.
    Product,

    /// Узел — может содержать узлы и детали.
    Unit,

    /// Деталь — конечный элемент без вложенных компонентов.
    Part
}
